import Foundation
import UIKit

class ColorStyle {
    class func tintColor() -> UIColor {
        return UIColor.red
    }
    class func navigationBarTintColor() -> UIColor {
        return UIColor(red: 150.0/255.0, green: 105.0/255.0, blue: 161.0/255.0, alpha: 1)
    }
}
