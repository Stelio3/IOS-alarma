import Foundation
import UIKit

class ColorStyle {
    class func tintColor() -> UIColor {
        return UIColor.orange
    }
    class func navigationBarTintColor() -> UIColor {
        return UIColor(red: 0/255.0, green: 0/255.0, blue: 0/255.0, alpha: 1)
    }
}
